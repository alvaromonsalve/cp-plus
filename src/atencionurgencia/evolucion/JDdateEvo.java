    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(validar()){
            int hora = jSpinField1.getValue();
            int min = jSpinField2.getValue();
            GregorianCalendar calendar = new GregorianCalendar();
            calendar.setTime(jDateChooser1.getDate());
            calendar.set(GregorianCalendar.HOUR_OF_DAY, hora);
            calendar.set(GregorianCalendar.MINUTE, min);
            fecha_hora = calendar.getTime();
            this.dispose();
        }            
    }//GEN-LAST:event_jButton1ActionPerformed

