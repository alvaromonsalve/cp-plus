    private boolean validar(){
        try {
            Date date = jDateChooser1.getDate();
            MyDate.ddMMyyyy.format(date);
            return true;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "La fecha de Evolución no es valida: \n"+e.getMessage());
            return false;
        }
    }

